#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char			*str;
	int				i;
	int				j;
	unsigned int	len;

	len = ft_strlen((char *)s1);
	str = (char *)malloc(sizeof(*str) * (len + ft_strlen((char *)s2) + 1));
	if (!str)
		return (NULL);
	i = 0;
	j = 0;
	while (s1[i] != '\0')
	{
		str[i] = s1[i];
		i++;
	}
	while (s2[j] != '\0')
		str[i++] = s2[j++];
	str[i] = '\0';
	return (str);
}
