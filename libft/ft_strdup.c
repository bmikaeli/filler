#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*copy;
	int		length;
	int		i;

	length = ft_strlen(s1);
	copy = (char *)malloc(sizeof(*copy) * (length + 1));
	if (!copy)
		return (NULL);
	i = 0;
	while (s1[i] != '\0')
	{
		copy[i] = s1[i];
		i++;
	}
	copy[i] = s1[i];
	return (copy);
}
